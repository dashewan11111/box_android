package com.adult.android.entity;

public class ReplyDto {

	private String replierId;

	private String topicId;

	private String replierName;

	private String replierImgUrl;

	private String level;

	private String replyTime;

	private String content;

	private String approveCount;

	private String floor;

	public String getReplierId() {
		return replierId;
	}

	public void setReplierId(String replierId) {
		this.replierId = replierId;
	}

	public String getTopicId() {
		return topicId;
	}

	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}

	public String getReplierName() {
		return replierName;
	}

	public void setReplierName(String replierName) {
		this.replierName = replierName;
	}

	public String getReplierImgUrl() {
		return replierImgUrl;
	}

	public void setReplierImgUrl(String replierImgUrl) {
		this.replierImgUrl = replierImgUrl;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getReplyTime() {
		return replyTime;
	}

	public void setReplyTime(String replyTime) {
		this.replyTime = replyTime;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getApproveCount() {
		return approveCount;
	}

	public void setApproveCount(String approveCount) {
		this.approveCount = approveCount;
	}

	public String getFloor() {
		return floor;
	}

	public void setFloor(String floor) {
		this.floor = floor;
	}

}
